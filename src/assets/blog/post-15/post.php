                    <div class="min-quote">
                        <strong>Еще один фан факт:</strong> элитная проститутка никогда не признает то, что она проститутка. Она скорее подробно на пальцах расскажет о том, чем отличается проститутка от распутной женщины. Кстати, некоторые из них верят в идеальный брак - кому-то это удается. Но в большинстве своем, их деятельность бесследно не проходит.
