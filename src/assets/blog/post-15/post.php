                        Промискуитетка <strong>сознательно</strong> выбирает, с кем вступать в связь. Для нее имеет значение привлекательность, внешние факторы партнера. Нимфоманка же во время очередной вспышки (а такие эпизоды могут происходит десятки раз каждый день) может лечь с тем, кто попадется под руку без разбора.
                    </p><br>
                    <p><strong>Причины</strong></p>
                    <p>
                        <ul class="numeric-list">
                            <li>
                                Патологическое повышенное сексуальное влечение/чрезвычайно высокое либидо. Это может быть связано с нарушением работы головного мозга человека
                            </li>
