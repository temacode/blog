                    <div class="text-header">Промискуитет. Каковы причины и последствия</div>
                    <img src="post-img-2.jpg" class="img-full-width">
                    <p>
                        <strong>Промискуитет</strong> это беспорядочные половые связи. В психологии и сексологии промискуитет относят к сексуальной патологии/расстройству.
                    </p><br>
                    <p>
                        В первую очередь, промискуитет характерен как женщинам, так и мужчинам любой сексуальной ориентации. Как мы знаем, мужские «похождения» в обществе не осуждаются, более того, многие убеждены, что мужской промискуитет заложен в мужчине природой. А что касается женщины…такое поведение недопустимо и даже может быть наказуемым. Как и в параграфе о проституции, так и здесь будем рассматривать Женщину в качестве объекта исследования. Мужчины, ничего личного :)
                    </p><br>
                    <p><strong>Чем отличается промискуитет от нимфомании?</strong></p>
                    <p>
                        Эти два схожие по смыслу понятия часто путают. Может быть вы или кто-то из ваших подруг могли в качестве объяснения своего повышенного интереса к половым связям привести следующий аргумент (в шутку): «Ах, я просто нимфоманка!»
                    </p><br>
                    <p>
                        Действительно, «нимфоманка» звучит кокетливо и более презентабельно, чем «промискуитетка». Но на деле ничего очаровательного нет ни в первом, ни во втором случаях.
                    </p><br>
                    <p>
                        Многочисленные половые связи - это то, что их объединяет. А теперь обо всем по порядку.
