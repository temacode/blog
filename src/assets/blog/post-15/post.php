                    <p>
                        Но все же отмечу: деньги, заработанные этим ремеслом, принято считать легкими. Насколько это правда и правда ли это? (риторический вопрос)
                    </p><br>
                    <p>
                        Начну с самых настоящих научных фактов. С точки зрения клинической психологии, проституция является психическим расстройством (шизотипическим). Сразу оговорюсь, к этому будет относится также и промискуитет, природа которого мало чем отличается от данного явления (об этом позднее)
                    </p><br>
                    <div class="text-header">Портрет проститутки. 2 типа</div>
                    <p>
                        Данное ремесло выбирают «толстокожие» особы, относящиеся к сексу хладнокровно и спокойно, не романтизируют его. Таких людей проституция особым образом не травмирует
                    </p><br>
                    <p><strong>«Только бизнес и ничего кроме»</strong></p>
                    <p>
                        Таким женщинам удается неплохо устроить личную жизнь. Они выходят замуж, создают семьи. Однако, бывают случаи, что и это не мешает продолжать промышлять этим делом в параллель. Совесть не мучает. Также отмечу, что у такого типа женщин связь между телом и душой очень слабая (скорее отсутсвует вовсе).
                    </p><br>
                    <p><strong>Женщина с тонкой душевной организацией</strong></p>
                    <p>
                        Да, и такие «смельчаки» умудряются стать частью этого совсем не легкого бизнеса. Проституция разрушает именно их: ранимых, впечатлительных, эмоциональных, внушаемых и ведомых. Такие девушки подвержены затяжным депрессиям и суицидальным намерениям. Оставаться «на плаву» помогают вредные привычки: алкоголь - в лучшем случае. Сразу назревает вопрос: что же если они так мучаются и страдают, они продолжают заниматься сексом за деньги?
                    </p><br>
                    <p>
                        А вот и ответ: что первый, что второй типы обладают садомазохистскими наклонностями. Это их объединяет. Разница лишь в толщине «кожи» и выносливости :)
                    </p><br>
                    <p>
                        «Я никак иначе деньги заработать не могу», «если бы не такие легкие деньги, я…» - это в большинстве случаев отговорки. К тому же, многие обеспеченные женщины могут также этим промышлять. Мотивов решения пойти в данную профессию может быть масса. Однако, в основе зачастую лежат проблемы из детства, дето-родительские отношения.
                    </p><br>
                    <div class="min-quote">
                        <strong>Фан (или не очень) факт:</strong> некоторые проститутки могут искренне любить одного человека, ощущать преданность ему (то есть бесплатно заниматься сексом только с ним)
