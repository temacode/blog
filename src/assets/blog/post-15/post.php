                    <div class="text-header">Проституция. Причины и мотивы</div>
                    <img src="post-img-1.jpg" class="img-full-width">
                    <p>
                        Романтизировать и рассуждать на тему того, насколько это профессия древняя - не будем. Все мы все понимаем. Однако, сразу попрошу отключить поток оценочных суждений. Напомню, мы здесь исследуем явления с разных сторон. Исследователи, а не судьи :)
