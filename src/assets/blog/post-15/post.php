                    <div class="text-header">Виды проституции?</div>
                    <p>
                        Разумеется, как и в обычном мире, так и в мире проституции у каждого обывателя свой уровень, свои желания и намерения. Есть проститутки, еле-еле сводящие концы с концами (те, что обычно раньше встречались на обочине трасс), есть «пташки» высоких полетов. Ко второму случаю может относится все, что угодно: такие девушки помимо денежной компенсации могут вступать в половые связи за подарки, дорогие вещи, поездки и тд. Иными словами, публичные женщины различаются уровнем материальных запросов (кто на какой глубине плавает), но суть остается одна. Но так или иначе, их всех объединяет одно - скудность духовных запросов.
                    </p><br>
                    <p>
                        Психологические исследования показали, что им свойственны агрессия, эгоцентризм, незрелость, озабоченность материальными критериями своего статуса.
                    </p><br>
                    <p>
                        Женщины, занимающиеся проституцией любого рода, как правило, ощущают собственную неполноценность, неуверенность в себе, чувствуют себя незначимыми. Защитными механизмами, способными компенсировать эти дефициты, обычно выступают вышеупомянутые «компоненты» - эгоцентризм и агрессия.
                    </p>
