                                Женщины истероидного и демонстративного типа личности  склонны к частой смене половых партнеров. В данном случае причиной является особенность характера
                            </li>
                            <li>
                                Страх потери. Женщина боится быть отвергнутой, потерять возлюбленного или пережить измену, поэтому на подсознательном уровне решается на то, чтобы вообще ни к кому не привязываться, не испытывать искренних чувств к партнеру
                            </li>
                            <li>
                                Травматический сексуальный опыт. Жертвы изнасилования могут также быть склонными к промискуитету
                            </li>
                            <li>
                                Измена. Такое явление может вызвать у женщины желание отомстить. Месть может быть как осознанной, так и бессознательной
                            </li>
                            <li>
                                Сексуальные впечатления. Поиск новых сексуальных «путешествий» может закрепиться в поведение и выльется в промискуитет
                            </li>
                        </ul>
                    </p><br>
                    <div class="text-header">Нимфомания. Что скрывается под нежным и безобидным названием?</div>
                    <img src="post-img-3.jpg" class="img-full-width">
                    <p>
                        Нимфомания - это женская гиперсексуальность, патологическое усиленное половое влечение. Отмечу еще раз - ЖЕНСКАЯ, то есть нимфоманов не бывает :)
                    </p><br>
                    <p>
                        Существует еще название этой девиации - бешенство матки. Платон решил не драматизировать!<br>
                        Он считал, что матка - это бешенный и всепоглощающий зверь, который поселился в теле женщины и желает немедленно исполнить детородные функции. Когда «зверю» не удается удовлетворить потребность (зачатие), он приходит в бешенство и, как говорится, мало никому не покажется.
                    </p><br>
                    <p><strong>Происхождение термина «нимфомания»</strong></p>
                    <p>
                        За романтичным названием далеко не сладкого заболевания стоит вполне себе поэтичная история, а точнее древнегреческий миф: сверхсексуальные нимфы заманивали мужчин в лес, чтобы удовлетворить свою похоть. С путниками они вытворяли такое, что они едва приходили в себя после соития. Мужчина, оказавшийся участником сексуальных отношений с лесной нимфой, был обречён лишится ощущения удовлетворения с другими женщинами.
                    </p><br>
                    <div class="min-quote">
                        <strong>Фан факт:</strong> существует мнение, что именно лесные развратные нимфы познакомили людей с такими сексуальными утехами, как оральный и анальный секс.
                    </div>
                    <div class="quote">
                        «Кто отведал любви нимфы, тот не сможет позабыть ее ласки никогда»<br><br>
                        Историк Геродот
                    </div>
                    <p>
                        Мифы мифами, но данная девиация имеет мало чего красивого и приятного в реальности. Нимфомания приносит боль не только обладательнице сего недуга, но и половым партнерам. Как уже ранее было сказано, женщины страдающие этим заболеванием не могут достичь полового удовлетворения (при многочисленных оргазмах). Следовательно, запросы нимфоманки настолько завышаются, что они оказываются не под силу возможностям партнера.
                    </p><br>
                    <p>
                        Мужчины, которые думают, что нимфоманка - это идеальная любовница, которая хочет секса всегда и везде, я должна вас огорчить. Во-первых, это достаточно редкое заболевание в чистом виде. Обычно нимфомания - это следствие шизофрении, психопатии и других расстройств. Чаще всего повсеместный промискуитет по ошибке называют «нимфоманией» (звучит красивее). Во-вторых, стоит различать женщин с повышеным либидо и тех, кто действительно страдает нимфоманией. Имеет смысл различать хотя бы потому что нимфы глубоко несчастные и неудовлетворённые люди. Они способны вступить в половую связь без разбора в любой ситуации из-за патологического навязчивого сексуального желания.
                    </p><br>
                    <p><strong>Признаки</strong></p>
                    <p>
                        <ul class="line-list">
                            <li>постоянная сексуальная неудовлетворенность</li>
                            <li>непрекращающиеся сексуальное фантазирование</li>
                            <li>возможность вступить в случайные связи без разбора</li>
                            <li>
                                острая необходимость секса (иногда нимфоманке недостаточно и 20 совокуплений в день)
                            </li>
                            <li>
                                внешность, привлекательность, пол и уж тем более возраст партнёра  - не являют собой значимость при выборе партнера для соития
                            </li>
                        </ul>
                    </p><br>
                    <p>
                        Страстные девушки, думающие, что они нимфоманки, обращаюсь к вам с трепетом и большой любовью! Если вышеперечисленные пункты не сходятся с вашей ситуацией - don’t  care! Скорее всего у вас просто повышенное либидо. Гиперсексуальные женщины составляют всего 15% , а нимфоманки - всего 3%.
                    </p><br>
                    <p><strong>Типы нимфомании</strong></p>
                    <p>
                        <ul class="numeric-list">
                            <li>
                                Врожденная. Нимфомания проявляется в юном возрасте. Вспомните набоковскую  Лолиту («Лолита» В.В. Набоков), не просто так автор ее ласково прозвал «нимфеткой». Девочки рано начинают половую жизнь  и часто меняют партнеров
