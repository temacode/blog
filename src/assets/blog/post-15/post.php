                            </li>
                        </ul>
                    </p><br>
                    <p><strong>Портрет «промискуитетки»</strong></p>
                    <p>
                        Ранимость и уязвимость - это то, что отличает таких женщин. Все идет из детства, и это явление - не исключение. Скорее всего в детстве девочки не чувствовали себя в безопасности, не ощущали родительскую любовь (особенно отцовской). Это исказило их мировосприятие. Во взрослом возрасте у женщины формируется ложное представление о самоценности и женского предназначения в целом.
                    </p><br>
                    <p>
                        Основной причиной женского промискуитета можно считать отсутсвие ощущения нужности, безопасности и внимания. Также существуют и ряд других мотивов:
                        <ul class="line-list">
