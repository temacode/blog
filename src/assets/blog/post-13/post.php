                    <div class="text-header">Что такое сепарация?</div>
                    <p>
                        Сепарация - это отделение ребенка от родителя. В первую очередь это природный процесс, который связан с независимостью как материальной, так и эмоциональной, психологической - одним словом, <strong>целостной</strong>.
                    </p><br>
                    <p>
                        Человек считается сепарированным тогда, когда он больше не ищет одобрения, признания или какой-либо другой родительской оценки. Человек - отдельная самостоятельная единица со своим индивидуальным набором: мировоззрением, мировосприятием, интересами, взглядами и желаниями.
                    </p><br>
                    <p>
                        Если вы думаете, что тема сепарации от родителей касается только молодых людей, которые вот-вот вылетели из «мамского» гнезда, вы ошибаетесь. Проблема касается всех.
                    </p><br>
                    <p>
                        Также финансовая независимость человека и его отдельное проживание - еще не показатель сепарации.
                    </p><br>
                    <p>
                        Сразу приведу пример. Скорее всего у вас есть среди знакомых мужичок или взрослая женщина, которые всю жизнь прожили с мамой. Вопрос: на каком уровне их сепарация? Есть ли она вообще? И какие беды в таком случае ее отсутсвие несет?
                    </p><br>
                    <p>
                        Из-за сильной привязанности к родителям человеку трудно выстроить здоровые отношения с партнером и даже с собственными детьми. Что уж тут говорить, личные границы несепарированного человека нарушены, он их не чувствует (а если и чувствует, то слабо-вяло).
                    </p>
                    <img src="post-img-2.jpg">
                    <div class="text-header">Зачем нужна сепарация?</div>
                    <p>
                        В первую очередь она нужна для того, чтобы ощущать себя всецело, четко отделять свои желания, мотивы, решения и интересы от навязанного, от того, что по сути принадлежит маме/папе или партнеру (жене/мужу). Сепарация позволяет чувствовать себя «отдельным» человеком, а не частью родителей (есть вы и ваши тараканы в голове, а есть они со своим «зоопарком»). Также она дает внутреннее ощущение свободы, позволяющее принимать и любить себя безусловно.
                    </p>
