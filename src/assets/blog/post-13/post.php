                    </p><br>
                    <div class="text-header">Зависимость от родителей. Причины</div>
                    <p>
                        <ul class="numeric-list">
                            <li>
                                Родительское равнодушие. Родители не интересуются жизнью своего ребенка, а ему в свою очередь хочется ощущать любовь, чувствовать поддержку и заботу.
                            </li>
                            <li>
                                Чрезмерная опека. Думаю, вы сами уже догадались, что речь идет о комнатных растениях, которых уже изначально «нафаршировали» родители с ног до головы. Такой человек плохо приспособлен к самостоятельной жизни в социуме. Таким людям свойственно убегать от проблем в случае их появления. Куда проще вернуться в родительское гнездо, где безопаснее и где все и так само решится без собственного участия, нежели в кои-то веки самому начать предпринимать действия, возможно чем-то рискуя
                            </li>
                            <li>
                                Жертвенная мать. Обычно такой «лихорадкой» страдают именно матери, лежащие мертвым грузом на плечах детей. Именно такая мать имеет тенденцию считать, что дети ей «должны». Любой шаг вправо - шаг влево от представлений и ожиданий мамы непременно обречен  на такие манипулятивные фразочки как: «Я тебе жизнь подарила, а ты неблагодарный…». Иными словами - откровенное внушение виновности ребенку. Как мы знаем, чувства страха, стыда и виновности - одни из самых мощнейших рычагов манипуляции. Так вот, человек под таким давлением стремится делать все согласно «инструктажу» мамы, дабы не обидеть ее и сделать так, чтобы она была довольна. Ну, а теперь представьте, что чувствует человек, выросший в такой семье? Какого это проживать чужой сценарий собственной жизни?
                            </li>
                            <li>
                                Тираничный подход в воспитании ребенка. Бесконечные претензии, недовольства, ругань и даже рукоприкладство … Из этого следует навязчивая идея ребенка доказать свою ценность родителю. Начинается борьба за любовь. За ЛЮБОВЬ. За то, что он и так заслужил. Ребенок старается изо всех сил, но, увы, все это ничто в глазах семьи. Привычка показывать свою значимость переносится уже во взрослый возраст. Результата от этого, конечно, же ноль. Однако, ребенок таких родителей обречен до конца дней доказывать всему миру свою ценность, даже если их уже нет в живых. Ах, да! Обречен до тех пор пока не осознает, что пришло время сепарации :)
                            </li>
                        </ul>
                    </p><br>
                    <div class="text-header">Что делать и как экологично сепарироваться?</div>
                    <img src="post-img-5.jpg">
                    <p>
                        <ul class="numeric-list">
                            <li>
                                Признание. Время признаться самому себе, что эмоциональная зависимость от родителей присутствует. Рекомендую подробно расписать ваши представления о своей жизни, личные желания, четко отделяя родительские представления о вас и вашей жизни - это сейчас не имеет значения. В чем конкретно вы чувствуете зависимость от родителей?
                            </li>
                            <li>
                                Осознание. Ты и твои родители - разные люди. Здесь я также рекомендую не убирать далеко блокнот и ручку, подробно прописать по пунктам, чем вы отличаетесь от мамы и папы. Правило: избегаем оценочных суждений, пишем кратко и тезисно. Чем больше - тем лучше.
                            </li>
                            <li>
                                Личные границы. Время действовать интуитивно в этом вопросе истекло. Сейчас тот момент, когда следует продумать и сформулировать ряд определенных правил: как можно вести себя по отношению к вам, а как нельзя. Разрешите себе защищать свои границы и не позволять другим их нарушать. Важно спокойно и конструктивно объяснить семье, что приемлемо, а что нет для вас.
                            </li>
                            <li>
                                Благодарность. Составь развернутый список того, за что вы благодарны своей семье, родителям.
                            </li>
                            <li>
                                Сценарий жизни. Человек имеет право быть таким, каким он есть; жить так, как желается. Это касается и родителей, и вас. Смысла пытаться переделать друг друга мало (бесполезное занятие).
                            </li>
                        </ul>
                    </p>
