                        что-то от нарцисса. Это будет являться нормой до тех пор пока нарциссическая часть не начнет составлять основу личности.
                    </p><br>
                    <p>
                        Портрет нарцисса: выглядит умным, красивым, успешным, высокомерным. Однако, ему не свойственны чувства в общепринятом понимании. Например, чувство любви у нарцисса искажено: любить больно, страшно и унизительно. Куда безопаснее внушать любовь, а не испытывать. Также людям нарциссического типа личности свойственно ложное «Я». Об этом поподробнее.
                    </p><br>
                    <p>
                        Ложное «Я» - это некая подушка безопасности, то, что защищает от других его чувства, переживания, интересы и тд. Отсюда и яркий «симптом» сего недуга: позерство и подмена ценностей (деньги, карьерный рост, известность и пр).
                    </p><br>
                    <p>
                        Да, хладнокровность не отнять у нарцисса. Однако, в глубине души (где-то ооооочень далеко) он ощущает, что его механизмы дают сбой и вытесненное истинное «Я» слабо - вяло, но дает о себе знать. И по итогу даже эта собственноручно навешенная мишура в виде достижений и денег не удовлетворяет и не насыщает его в полной мере.
                    </p><br>
