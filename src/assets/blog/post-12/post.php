                    <p><strong>Женщина - шизоид</strong></p>
                    <p>
                        Она немногословна, предпочитает роль наблюдателя, а не участника. Не любит большое скопление людей. Женщинам этого типа свойственна неряшливость - это качество проявляется не у всех, у некоторых оно скорректировано путем воспитания.
                    </p><br>
                    <p>
                        В отношениях с мужчиной женщина - шизоид может вести себя непоследовательно и непредсказуемо. Не забываем о боязни сближения :)
                    </p><br>
                    <p>
                        У шизоидов проблемы с интуицией. Отсюда следует неразборчивость в людях.
                    </p><br>
                    <p><strong>Мужчина-шизоид</strong></p>
                    <p>
                        Снаружи он кажется неприступным и холодным. Однако, за всем этим скрывается глубочайший внутренний мир. Этот мужчина настоящая загадка, разгадать которую можно получить сокровище. Однако, не всем удается хотя бы глазком заглянуть в глубины его души и уж тем более его разгадать.
                    </p><br>
                    <p>
                        Он скуп на эмоции. Иногда его поведение кажется странным и непонятным. Дистанцирование от реальности - это его главная фишка.
                    </p><br>
                    <p>
                        Шизоид может с головой погрузится в работу, в компьютерные игры, в фантазии и тд.
                    </p><br>
                    <p>
                        В отношениях, как отмечают их избранницы, не слишком чувствительны и эмоциональны. Здесь может быть точка конфликта.
                    </p><br>
                    <p>
                        Принимая решение, шизоид не руководствуется целью или достижением желаемого результата. Он действует самопроизвольно и спонтанно. На вопрос «Почему ты это сделал?» - он не даст конкретный ответ.
                    </p><br>
                    <div class="text-header">Гипертимный тип личности</div>
                    <p>
                        «Тим» - настроение, «гипер» - повышенное. Да-да, речь идет о счастливчиках с повышенным настроением.
                    </p><br>
                    <p>
                        Главные черты: гиперактивность, лидерские качества, логичность, непредсказуемость, спонтанность и частая необдуманность.
                    </p><br>
                    <p>
                        Видит во всем положительные стороны, весел и свеж, как говорится. Гипертим гуляет на широкую ногу. Отсюда следует склонность к загулам (любого характера): будь это алкоголь или пристрастие к азартным играм.
                    </p><br>
                    <p>
                        В отличие от шизоида гипертим легко впускает в свою жизнь людей и также просто их отпускает. Возможны беспорядочные половые связи
                    </p><br>
                    <p>
                        Гипертим - человек хаос.
                    </p><br>
                    <p><strong>Женщина-гипертим</strong></p>
                    <p>
                        Нерелигиозная, веселая, легкая на подъем. Макияж и маникюр грубый. Она не парится по поводу своей внешности. Отдает предпочтение яркой и пестрой одежде.
                    </p><br>
                    <p>
                        Как говорится, гипертимки на любителя.
                    </p><br>
                    <p><strong>Мужчина-гипертим</strong></p>
                    <p>
                        Мужчина-гипертим, как и женщина - гипертим, не заботится о своей внешности. Красивый или не очень - he doesn’t care.
                    </p><br>
                    <p>
                        Гипертим хорошим вкусом не отличается. Иногда сочетания в одежде получаются весьма спорными: например, смокинг + кроссовки. Да и с чистоплотностью тоже есть некоторые вопросы. Мужчина - гипертим - это тот, за чистотой носков и футболок которого следит жена. Вся надежда на нее. Если и она будет такой же неряшливой, то все - пиши пропало :)
                    </p><br>
                    <div class="text-header">Эпилиптоидный тип личности</div>
                    <p>
                        На очереди любители порядка во всем, ко всему относятся расчетливо. Педантичны, уверены в своих решениях. Склонны ставить себя выше других. Отсюда следует эгоистичность и требовательность.
                    </p><br>
                    <p>
                        Характерные черты: умение получать выгоду, настойчивость, устремленность. Эпилептоиды умеют простраивать желаемый сценарий жизни и проживать его. Склонность к зависти. Также замечены в использовании людей для своей выгоды («цель оправдывает средства»).
                    </p><br>
                    <p>
                        Человек этого психотипа помешен на чистоте и порядке. Они не просто сами стремятся к этому, эпилептоид заставляет окружающих соблюдать правила. Беспорядок не на шутку может вывести его из себя. Эпилептоид агрессивен.
                    </p><br>
                    <p>
                        Мышление у представителей этого психотипа ясное, четкое и понятное всем окружающим. Плохо чувствует людей из-за неразвитой эмпатии.
                    </p><br>
                    <p><strong>Женщина-эпилиптоид</strong></p>
                    <p>
                        Она четко знает как жить и требует того же от других. Сильна и независима, на все руки мастер. Женщина этого типа стрессоустойчивая и по - мужски целеустремленная.
                    </p><br>
                    <p>
                        В качестве партнера женщине - эпилиптоиду подойдут мягкие партнеры, которые с радостью переложат на нее ответственность в каких-либо вопросах. В супружеской жизни занимают главенствующую роль. Это тот самый случай, когда карьера мужчины может зависеть от жены («вырастила из солдата генерала»).
                    </p><br>
                    <p><strong>Мужчина-эпилиптоид</strong></p>
                    <p>
                        Он властный и авторитарный. Чрезмерно бережлив. Такая бережливость граничит с жадностью. Раздражает эмоциональность (особенно женская). Долго помнит обиды
                    </p><br>
                    <p>
                        Выстраивать отношения с таким мужчиной очень трудно.Сделать это сможет та женщина, которая способна подчиняться, либо женщина - эпилиптоид
                    </p><br>
                    <p>
                        При знакомстве эпилептоид проявляет подозрительность. Это защитный механизм. Человек пытается понять: опасен или не опасен для него новый знакомый.
                    </p><br>
                    <div class="text-header">Астенический тип личности</div>
                    <p>
                        Представителям этого психотипа свойственен упадок сил (как на физическом уровне, так и на психическом), быстро утомляются без особо явных причин на это.
                    </p><br>
                    <p>
                        В отличии от истероида, который зачастую является зачинщиком конфликтов, астеник предпочитает отмалчиваться, дабы избежать споров. Они уступчивы.
                    </p><br>
                    <p>
                        Также у астеников отмечается излишняя впечатлительность, что зачастую является причиной тревожности.
                    </p><br>
                    <p>
                        Астеники наделены аналитически-сомневающимся мышлением. Среди них многие находят себя в науке. Представители данного психотипа совершили большой прорыв в исследовательской работе и искусстве. Среди известных личностей астениками также являются: Аристотель, Ньютон, Фрейд, Франкл, Шиллер и др.
                    </p><br>
                    <p>
                        Астеники - добрые, ответственные. Они приверженцы нравственных принципов и совестливости.
                    </p><br>
                    <div class="min-quote">
                        <strong>Женщины и мужчины -астеники мало чем отличаются друг от друга.</strong>
                    </div>
                    <p>
                        Обычно женщина этого психотипа худощавого телосложения, высокого роста. Обладательницы хрупкой психики. Блестящие интеллектуальные способности.
                    </p><br>
                    <p>
                        В них сочетает добро, альтруизм, духовность и любовь.
                    </p><br>
                    <p>
                        И женщины, и мужчины любят и стремятся к благотворительности.
                    </p><br>
                    <p>
                        Астеник подвержен большому риску раствориться в любви и впасть в созависимость (как в любовных отношениях, так и в дружеских). Как и женщина, так и мужчина часто испытывают расстройство из-за «несостыковки» ожидания и реальности.
                    </p><br>
                    <p>
                        Слабая нервная система является причиной низкой сексуальности.
                    </p><br>
                    <p>
                        Мужчины, как правило, являются интеллектуалами. Не отличаются уверенностью в себе, весьма мнительны.
