                        Истероидный тип личности
                    </div>
                    <p>
                        Истероид - это не тот, кто постоянно закатывает скандалы и истерит. Да, человек с таким типом психики склонен ярко выражать свои чувства и на эмоциях не скупиться на язвительные слова или даже бить посуду. Такова его природа, это одно из проявлений его характера.
                    </p><br>
                    <p>
                        В отличие от нарцисса, речь о котором пойдет далее, истероид не так сильно нуждается в восхищении окружающих. Он чувствительный, эмоциональный, переполнен переживаниями и выражает их максимально ярко и драматично (иногда даже слишком).
                    </p><br>
                    <p>
                        Главные черты: эгоцентризм, жажда постоянного внимания к своей персоне. Да что угодно, лишь бы только не безразличие и равнодушие. Перспектива быть незамеченным для истероида - нечто неприятное (мягко говоря). Человек с таким психотипом много энергии черпает извне и ровно столько же (если не больше) отдает в ответ.
                    </p><br>
                    <p>
                        Портрет истероида: зачастую это яркие и харизматичные люди, за которыми хочется наблюдать. Они умеют эпатировать. Из-за того, что истероид практически беспрерывно находится в игре «на зрителя» ему трудно проживать искренние глубокие чувства. Склонны к суевериям, религиозны. Отсюда следует факт, что истероиды подвержены влиянию.
                    </p><br>
                    <p>
